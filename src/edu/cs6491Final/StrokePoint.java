package edu.cs6491Final;

/**
 * StrokePoint class:
 * 	Stores a representation of a point and a radius at that point
 */

public class StrokePoint {
	
	public Point point;
	public Float radius;
	
	public StrokePoint(Point point, Float radius){
		this.point = point;
		this.radius = radius;
	}

}
